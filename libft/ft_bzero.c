#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t			i;
	unsigned char	*v;

	i = 0;
	v = (unsigned char *)str;
	while (i != n)
	{
		v[i] = '\0';
		i++;
	}
}
