#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (dst[i] != '\0')
	{
		i++;
	}
	while (src[c] != '\0')
	{
		dst[i] = src[c];
		i++;
		c++;
	}
	dst[i] = '\0';
	return (dst);
}
